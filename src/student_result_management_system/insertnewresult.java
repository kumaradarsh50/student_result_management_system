    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String phy = jTextField2.getText();
        String chem = jTextField3.getText();
        String math = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_management", "root", "root@123");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='" + rollno + "' ");
            
            if (rs.next()) {
                st.executeUpdate("insert into result (rollno, phy, chemistry,maths,em, dbms  ) values('" + rollno + "','" + phy + "','" + chem + "','" + math + "','" + em + "','" + dbms + "') ");
                JOptionPane.showMessageDialog(null, "Data updated successfully.");
                
                setVisible(false);
                new insertnewresult().setVisible(true);
                
            } else {
                JOptionPane.showMessageDialog(null, "This Roll number does not exist.");
            }

            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

